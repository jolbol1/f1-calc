export type BeatableTime = {
  lapTime: LapTime
  difficulty: string
}

export const getNearestBeatableTime = (
  epochTime: number,
  track: Track,
): BeatableTime => {
  const times = track!.aiTimes
  let closestVal = Infinity
  let beatableTime: BeatableTime = {
    lapTime: new LapTime(times['0']!),
    difficulty: '0',
  }
  for (const key in times) {
    if (Object.prototype.hasOwnProperty.call(times, key)) {
      const obj = times[key]!
      const curDiff = obj - epochTime
      if (curDiff < closestVal && curDiff >= 0) {
        closestVal = curDiff
        beatableTime.difficulty = key
        beatableTime.lapTime = new LapTime(obj)
      }
    }
  }
  return beatableTime
}

