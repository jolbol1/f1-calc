export const getNearestBeatableTime = (
  lapTimeSerial: number,
  track: string,
) => {
  const trackObj = TrackList[track]
  if (trackObj === undefined) {
    throw `Could not find track with name ${track}`
  }
  const times = trackObj.aiTimes
  let closestVal = Infinity
  let closest: { diff: string; time: number } = {
    diff: '0',
    time: times['0'] ?? 0,
  }
  if (times === undefined) {
    console.error('Could not find track times.')
    return
  }
  for (const key in times) {
    if (Object.prototype.hasOwnProperty.call(times, key)) {
      const obj = times[key] ?? 0
      const curDiff = obj - lapTimeSerial
      if (curDiff < closestVal && curDiff >= 0) {
        closestVal = curDiff
        closest = {
          diff: key,
          time: obj,
        }
      }
    }
  }
  return closest
}

